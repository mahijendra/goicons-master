toast.configure();

export default function Icons({ svgItem, handleMouse, showSvgTitle, index }) {
  // eslint-disable-next-line no-unused-vars
  const [showIcon, setShowIcon] = useState(true);

  // Copied to clipboard
  function CopyToClipboard() {
    let val =
      '<svg width="24" height="24" viewBox="0 0 32 32" fill="#FFFFFF" xmlns="http://www.w3.org/2000/svg"><path d="' +
      svgItem.d +
      '" fill="currentColor" fill-rule="evenodd" clipxrule="evenodd"></path></svg>';
    copy(val);
    toast.dark("Copied to Clipboard!", {
      position: toast.POSITION.BOTTOM_CENTER,
      autoClose: 1500,
      transition: Zoom,
    });
  }

  return (
    <div
      className={`svgItem ${index}`}
      onMouseEnter={(e) => handleMouse(e, index)}
      onMouseLeave={(e) => handleMouse(e, -1)}
      style={{ position: "relative" }}
    >
      <svg width="24" height="2" viewBox="0 0 32 32" fill="white">
        {showIcon ? <path d={svgItem.d}></path> : null}
      </svg>
      {showSvgTitle === index && (
        <Tippy interactive={true} content={svgItem.title}>
          <p
            style={{
              position: "absolute",
              top: "50%",
              left: "50%",
              transform: "translate(-50%, -50%)",
              color: "red",
              fontSize: "12px",
            }}
            className="text-justify"
            onClick={CopyToClipboard}
          >
            .
          </p>
        </Tippy>
      )}
    </div>
  );
}
